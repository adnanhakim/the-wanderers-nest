    public void fetch(){
        PreparedStatement ps = null;
        ResultSet rs = null;
        String selectquery = null;
        if(tablecb.getSelectedIndex() == 0)
            selectquery = "SELECT * FROM users";
            
        try{
            ps = MyConnection.getConnection().prepareStatement(selectquery);
            rs = ps.executeQuery();
            userstable.setModel(DbUtils.resultSetToTableModel(rs));
                
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    
    public void delete(){
        String email = textfield.getText();
        PreparedStatement ps;
        ResultSet rs;
        String deletequery = "DELETE FROM users WHERE Emailid like ?";
        String selectquery = "SELECT Contactno FROM users WHERE Emailid like ?";
            
        try{
            ps = MyConnection.getConnection().prepareStatement(selectquery);
            ps.setString(1, email);
            rs = ps.executeQuery();
            if(rs.next())
            {
                ps = MyConnection.getConnection().prepareStatement(deletequery);
                ps.setString(1, email);
                ps.executeUpdate();
                fetch();
            }
            else
                errorlabel.setText("Invalid Data");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }    

